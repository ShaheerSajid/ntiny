#!/usr/bin/env python3
"""Generate memory map files from design/common/mem_map.json.

Outputs:
  design/common/mem_map.svh          — SystemVerilog `define header
  design/common/mem_map.h            — C header (NTINY_ prefixed)
  software/common/link.ld                  — linker script (default profile)
  verification/riscof/ntiny/env/link.ld    — linker script (riscof profile)

Usage:
  python3 scripts/gen_mem_map.py              # from repo root
  python3 scripts/gen_mem_map.py --check      # verify generated files are up-to-date
"""

import json
import os
import sys
import argparse

REPO_ROOT = os.path.normpath(os.path.join(os.path.dirname(__file__), ".."))
JSON_PATH = os.path.join(REPO_ROOT, "design", "common", "mem_map.json")

BANNER = "// AUTO-GENERATED by scripts/gen_mem_map.py — DO NOT EDIT\n// Source: design/common/mem_map.json\n"
C_BANNER = BANNER.replace("//", "//")


def load_json():
    with open(JSON_PATH) as f:
        return json.load(f)


def hex32(val):
    """Convert hex-string or int to 0x prefixed 8-digit uppercase hex."""
    if isinstance(val, str):
        val = int(val, 16)
    return f"0x{val:08X}"


def sv_hex32(val):
    """SV-style 32'hXXXX_XXXX."""
    if isinstance(val, str):
        val = int(val, 16)
    h = f"{val:08X}"
    return f"32'h{h[0:4]}_{h[4:8]}"


def gen_svh(data):
    """Generate mem_map.svh content."""
    lines = [BANNER, ""]
    default = data["profiles"]["default"]

    # Overridable memory regions (ifndef guards)
    for region in ["IMEM", "DMEM", "BOOT"]:
        base_key = f"{region}_BASE"
        size_key = f"{region}_SIZE_BYTES"
        lines.append(f"// --- {region} ---")
        lines.append(f"`ifndef {base_key}")
        lines.append(f"  `define {base_key}     {sv_hex32(default[base_key])}")
        lines.append(f"`endif")
        lines.append(f"`ifndef {size_key}")
        lines.append(f"  `define {size_key} {sv_hex32(default[size_key])}")
        lines.append(f"`endif")
        lines.append(f"`define {region}_END        (`{base_key} + `{size_key} - 1)")
        lines.append(f"`define {region}_DEPTH      (`{size_key} / 4)")
        lines.append(f"`define {region}_ADDR_WIDTH $clog2(`{region}_DEPTH)")
        lines.append("")

    # Peripherals (fixed addresses, no guards)
    lines.append("// --- Peripherals ---")
    for name, info in data["peripherals"].items():
        lines.append(f"`define {name}_BASE        {sv_hex32(info['base'])}")
        lines.append(f"`define {name}_END         {sv_hex32(info['end'])}")
    lines.append("")

    # Special addresses
    lines.append("// --- Special ---")
    for name, addr in data["special"].items():
        lines.append(f"`define {name}     {sv_hex32(addr)}")
    lines.append("")

    return "\n".join(lines)


def gen_c_header(data):
    """Generate mem_map.h content."""
    lines = [
        BANNER,
        "",
        "#ifndef NTINY_MEM_MAP_H",
        "#define NTINY_MEM_MAP_H",
        "",
    ]
    default = data["profiles"]["default"]

    for region in ["IMEM", "DMEM", "BOOT"]:
        base_key = f"{region}_BASE"
        size_key = f"{region}_SIZE_BYTES"
        lines.append(f"#define NTINY_{base_key}       {hex32(default[base_key])}")
        lines.append(f"#define NTINY_{size_key}  {hex32(default[size_key])}")
    lines.append("")

    lines.append("// Peripherals")
    for name, info in data["peripherals"].items():
        lines.append(f"#define NTINY_{name}_BASE      {hex32(info['base'])}")
    lines.append("")

    lines.append("// Special")
    for name, addr in data["special"].items():
        lines.append(f"#define NTINY_{name}   {hex32(addr)}")
    lines.append("")

    # Backward-compatible aliases (existing code uses e.g. UART_BASE_ADDR)
    aliases = {
        "UART_BASE_ADDR":  "NTINY_UART_BASE",
        "TIMER_BASE_ADDR": "NTINY_TIMER_BASE",
        "GPIO_BASE_ADDR":  "NTINY_GPIO_BASE",
        "PLIC_BASE_ADDR":  "NTINY_PLIC_BASE",
        "SPI_BASE_ADDR":   "NTINY_SPI_BASE",
        "BASE_ADDR":       "NTINY_I2C_BASE",     # i2c_defs.h uses BASE_ADDR
        "pwm_base_addr":   "NTINY_PWM_BASE",
    }
    lines.append("// Backward-compatible aliases")
    for old, new in aliases.items():
        lines.append(f"#ifndef {old}")
        lines.append(f"  #define {old}  {new}")
        lines.append(f"#endif")
    lines.append("")

    lines.append("#endif // NTINY_MEM_MAP_H")
    lines.append("")
    return "\n".join(lines)


def gen_linker_script(data, profile_name):
    """Generate a linker script for the given profile."""
    profile = data["profiles"][profile_name]

    imem_base = hex32(profile["IMEM_BASE"])
    imem_size = hex32(profile["IMEM_SIZE_BYTES"])
    dmem_base = hex32(profile["DMEM_BASE"])
    dmem_size = hex32(profile["DMEM_SIZE_BYTES"])
    tohost    = hex32(data["special"]["TOHOST_ADDR"])

    if profile_name == "default":
        return gen_default_linker(imem_base, imem_size, dmem_base, dmem_size, tohost)
    elif profile_name == "riscof":
        return gen_riscof_linker(imem_base, imem_size, dmem_base, dmem_size, tohost)
    else:
        raise ValueError(f"Unknown profile: {profile_name}")


def gen_default_linker(imem_base, imem_size, dmem_base, dmem_size, tohost):
    """Default linker script for bare-metal software."""
    return f"""\
/* AUTO-GENERATED by scripts/gen_mem_map.py — DO NOT EDIT */
/* Source: design/common/mem_map.json (profile: default) */
OUTPUT_FORMAT("elf32-littleriscv", "elf32-littleriscv", "elf32-littleriscv")
GROUP(-lc -lgloss -lgcc -lm)
OUTPUT_ARCH(riscv)

MEMORY
{{
  ROM (rwx) : ORIGIN = {imem_base}, LENGTH = {imem_size}
  RAM (rwx) : ORIGIN = {dmem_base}, LENGTH = {dmem_size}
}}

ENTRY(_init)

SECTIONS
{{
  PROVIDE(__stack_top = ORIGIN(RAM) + LENGTH(RAM));

  .init           : {{ *(.init) }} > ROM
  .RESET_HANDLER  : {{ *(.RESET_HANDLER) }} > ROM
  .ISR_SOFT_ASM   : {{ *(.ISR_SOFT_ASM) }} > ROM
  .ISR_TIMER_ASM  : {{ *(.ISR_TIMER_ASM) }} > ROM
  .ISR_EXT_ASM    : {{ *(.ISR_EXT_ASM) }} > ROM
  .ISR_DEFAULT_ASM : {{ *(.ISR_DEFAULT_ASM) }} > ROM
  .text :
  {{
    *(.text.unlikely .text.*_unlikely .text.unlikely.*)
    *(.text.exit .text.exit.*)
    *(.text.startup .text.startup.*)
    *(.text.hot .text.hot.*)
    *(SORT(.text.sorted.*))
    *(.text .stub .text.* .gnu.linkonce.t.*)
    *(.gnu.warning)
  }} > ROM
  PROVIDE (__etext = .);
  PROVIDE (_etext = .);
  PROVIDE (etext = .);

  .rodata : {{ *(.rodata .rodata.* .gnu.linkonce.r.*) }} > RAM
  .data :
  {{
    __DATA_BEGIN__ = .;
    *(.data .data.* .gnu.linkonce.d.*)
    SORT(CONSTRUCTORS)
    __SDATA_BEGIN__ = .;
    *(.srodata.cst16) *(.srodata.cst8) *(.srodata.cst4) *(.srodata.cst2) *(.srodata .srodata.*)
    *(.sdata .sdata.* .gnu.linkonce.s.*)
  }} > RAM

  _edata = .; PROVIDE (edata = .);
  . = .;
  __bss_start = .;

  .sbss :
  {{
    *(.dynsbss)
    *(.sbss .sbss.* .gnu.linkonce.sb.*)
    *(.scommon)
  }} > RAM
  .bss :
  {{
    *(.dynbss)
    *(.bss .bss.* .gnu.linkonce.b.*)
    *(COMMON)
    . = ALIGN(. != 0 ? 32 / 8 : 1);
  }} > RAM

  . = ALIGN(32 / 8);
  . = SEGMENT_START("ldata-segment", .);
  . = ALIGN(32 / 8);
  __BSS_END__ = .;
  __global_pointer$ = MIN(__SDATA_BEGIN__ + 0x800,
                      MAX(__DATA_BEGIN__ + 0x800, __BSS_END__ - 0x800));

  /* tohost at fixed bus address */
  .tohost {tohost} (NOLOAD) : {{ *(.tohost) }}

  _end = .; PROVIDE (end = .);
}}
"""


def gen_riscof_linker(imem_base, imem_size, dmem_base, dmem_size, tohost):
    """RISCOF compliance linker script."""
    return f"""\
/* AUTO-GENERATED by scripts/gen_mem_map.py — DO NOT EDIT */
/* Source: design/common/mem_map.json (profile: riscof) */
OUTPUT_ARCH( "riscv" )
ENTRY(rvtest_entry_point)

MEMORY
{{
  IMEM (rwx) : ORIGIN = {imem_base}, LENGTH = {imem_size}
  DMEM (rwx) : ORIGIN = {dmem_base}, LENGTH = {dmem_size}
}}

SECTIONS
{{
  .text.init : {{ *(.text.init) }} > IMEM
  . = ALIGN(0x100);
  .text : {{ *(.text) }} > IMEM

  .data : {{ *(.data) *(.data.*) *(.data.string) }} > DMEM
  .sdata : {{ *(.sdata) *(.sdata.*) }} > DMEM
  .bss (NOLOAD) : {{ *(.bss) *(.bss.*) }} > DMEM

  /* tohost at fixed bus address — no backing memory needed, testbench monitors bus writes */
  .tohost {tohost} (NOLOAD) : {{ *(.tohost) }}

  _end = .;
}}
"""


def write_if_changed(path, content):
    """Write file only if content differs. Returns True if written."""
    abs_path = os.path.join(REPO_ROOT, path)
    os.makedirs(os.path.dirname(abs_path), exist_ok=True)
    try:
        with open(abs_path) as f:
            if f.read() == content:
                return False
    except FileNotFoundError:
        pass
    with open(abs_path, "w") as f:
        f.write(content)
    return True


def main():
    parser = argparse.ArgumentParser(description="Generate memory map files from mem_map.json")
    parser.add_argument("--check", action="store_true",
                        help="Check if generated files are up-to-date (exit 1 if stale)")
    args = parser.parse_args()

    data = load_json()

    outputs = {
        "design/common/mem_map.svh":
            gen_svh(data),
        "design/common/mem_map.h":
            gen_c_header(data),
        "software/common/link.ld":
            gen_linker_script(data, "default"),
        "verification/riscof/ntiny/env/link.ld":
            gen_linker_script(data, "riscof"),
    }

    if args.check:
        stale = []
        for path, content in outputs.items():
            abs_path = os.path.join(REPO_ROOT, path)
            try:
                with open(abs_path) as f:
                    if f.read() != content:
                        stale.append(path)
            except FileNotFoundError:
                stale.append(path)
        if stale:
            print("ERROR: Generated files are stale. Run: python3 scripts/gen_mem_map.py")
            for p in stale:
                print(f"  {p}")
            sys.exit(1)
        else:
            print("OK: All generated files are up-to-date.")
            sys.exit(0)

    wrote = 0
    for path, content in outputs.items():
        if write_if_changed(path, content):
            print(f"  wrote {path}")
            wrote += 1
        else:
            print(f"  unchanged {path}")
    print(f"Done. {wrote}/{len(outputs)} files updated.")


if __name__ == "__main__":
    main()
